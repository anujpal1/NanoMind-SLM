"""Run a bounded metadata audit on the streamed CodeParrot dataset."""
from collections import Counter
from itertools import islice
from math import isfinite
from pathlib import Path
from statistics import mean
from typing import Any

import yaml
from datasets import IterableDataset, load_dataset

CONFIG_PATH = Path("configs/data.yaml")

def parse_size(value: Any) -> int | None:
    """Convert a valid numeric dataset size to an integer."""
    if value is None or isinstance(value, bool):
        return None

    try:
        numeric_value = float(value)
    except (TypeError, ValueError):
        return None

    if not isfinite(numeric_value) or numeric_value < 0:
        return None

    return int(numeric_value)


def load_config() -> dict[str, Any]:
    """Load and validate the dataset configuration."""
    with CONFIG_PATH.open(encoding="utf-8") as file:
        config = yaml.safe_load(file)

    if not isinstance(config, dict):
        raise TypeError("Dataset configuration must be a dictionary.")

    return config


def main() -> None:
    """Inspect a limited number of records without saving source code."""
    config = load_config()
    dataset_config = config["dataset"]
    audit_config = config["audit"]

    dataset_id = str(dataset_config["id"])
    split = str(dataset_config["split"])
    sample_limit = int(audit_config["sample_limit"])
    streaming = bool(dataset_config["streaming"])

    if not streaming:
        raise ValueError("Streaming must remain enabled.")

    if sample_limit < 1 or sample_limit > 1000:
        raise ValueError("Audit sample_limit must be between 1 and 1000.")

    print(f"Dataset: {dataset_id}")
    print(f"Split: {split}")
    print(f"Maximum records: {sample_limit}")
    print("Raw source storage: disabled")

    dataset = load_dataset(
        dataset_id,
        split=split,
        streaming=streaming,
    )

    if not isinstance(dataset, IterableDataset):
        raise TypeError("Expected a streaming IterableDataset.")

    license_counts: Counter[str] = Counter()
    file_sizes: list[int] = []
    autogenerated_count = 0
    missing_content_count = 0
    observed_count = 0

    for sample in islice(dataset, sample_limit):
        observed_count += 1

        license_name = str(sample.get("license") or "unknown")
        license_counts[license_name] += 1

        size = parse_size(sample.get("size"))
        if size is not None:
            file_sizes.append(size)

        if sample.get("autogenerated") is True:
            autogenerated_count += 1

        content = sample.get("content")
        if not isinstance(content, str) or not content.strip():
            missing_content_count += 1

    print("\nAudit summary")
    print(f"  records inspected: {observed_count}")
    print(f"  records with integer size: {len(file_sizes)}")
    print(f"  autogenerated records: {autogenerated_count}")
    print(f"  missing or empty content: {missing_content_count}")

    if file_sizes:
        print(f"  minimum file size: {min(file_sizes):,} characters")
        print(f"  maximum file size: {max(file_sizes):,} characters")
        print(f"  average file size: {mean(file_sizes):,.2f} characters")

    print("\nLicense counts")
    for license_name, count in license_counts.most_common():
        print(f"  {license_name}: {count}")

    if observed_count != sample_limit:
        raise RuntimeError(
            f"Expected {sample_limit} records, received {observed_count}."
        )

    if not file_sizes:
        raise RuntimeError("No valid file sizes were found.")

    print("\nBounded metadata audit: PASSED")


if __name__ == "__main__":
    main()